<template>
  <v-card class="ma-4" height="600">
    <v-card-text>
      <h2 class="text-center my-3">今日やったこと</h2>
      <v-form class="mx-5 my-10">
        <v-select :items="tags" v-model="tagChecked" label="タグ"></v-select>
        <p class="text-center">
          何時間作業した？
          <span class="title">- {{ working_time }}h -</span>
        </p>
        <v-slider v-model.number="working_time" max="12"></v-slider>
        <v-textarea
          outlined
          v-model="text"
          :rules="textRules"
          label="内容"
        ></v-textarea>

        <div class="text-center mb-10">
          <v-btn @click="form_init">キャンセル</v-btn>
          <v-btn color="info" class="ml-2" @click="save">記録</v-btn>
        </div>
      </v-form>
    </v-card-text>
  </v-card>
</template>

<script>
import { mapActions } from "vuex";

export default {
  data() {
    return {
      tags: ["仕事", "勉強", "課題"],
      tagChecked: "",
      working_time: 4,
      text: "",
      now: new Date(),
      textRules: [v => !!v || "入力必須です"]
    };
  },
  methods: {
    save() {
      // 何もなかったら弾こうね あとで
      // if(!text) return
      const work_data = {
        tag: this.tagChecked,
        time: this.working_time,
        text: this.text,
        createAt: this.now
      };
      const times = {
        sum: this.working_time,
        work_sum: 0,
        study_sum: 0,
        task_sum: 0
      };
      this.addWork(work_data);
      this.addTime(times);
      console.log("Add : " + times.sum);

      this.$router.push({ name: "Recode" });
    },
    form_init() {
      this.tagChecked = "";
      this.time = "";
      this.text = "";
    },
    ...mapActions(["addWork", "addTime"])
  }
};
</script>
